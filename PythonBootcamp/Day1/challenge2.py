# Challenge to perform the swap of the values of 2 variables

a = input("a: ") # Enter the value of the variable a
b = input("b: ") # Enter the value of the variable b

# Sequence of instructions that performs the swap of the values
c = a
a = b
b = c

# Printing the values of the swapped variables
print("a = " + a)
print("b = " + b)