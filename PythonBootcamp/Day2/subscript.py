# Accepts User Entry and Stores it as a String Value
number = input("Enter any two digit number \n")
print()
# Splitting and Conversion of Values to Integer Numbers
first_digit = int(number[0])
second_digit = int(number[1])

# Prints the Split Values that the User Entered
print(first_digit)
print(second_digit)

# Sums up the Individual Values
sum_of_numbers = first_digit + second_digit

# Prints the Sum of the 2 Values
print(sum_of_numbers)

